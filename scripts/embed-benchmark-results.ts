// Reads docs/character-emergence-results.json and emits a TypeScript
// module the bundle can import for the IdentityInspector's
// cross-model-consistency section. Keeps the published benchmark
// result auditable from the running app, not just docs/.
//
// Run after run-cross-model-benchmark.ts:
//   npx tsx scripts/embed-benchmark-results.ts

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(here, "..");
const JSON_IN = path.join(ROOT, "docs", "character-emergence-results.json");
const TS_OUT = path.join(ROOT, "src", "lib", "identity", "published-benchmark.ts");

async function main(): Promise<void> {
  const raw = await readFile(JSON_IN, "utf8");
  const parsed = JSON.parse(raw) as {
    methodology: {
      character: string;
      participants: string[];
      judge: string;
      scenes: { id: string; label: string }[];
      ran_at: string;
    };
    aggregate: {
      character_id: string;
      ran_at: string;
      cross_provider_variance: number;
      cross_provider_stddev: number;
      per_provider: {
        provider_id: string;
        mean_overall: number;
        scene_count: number;
        per_dimension_mean: {
          trait_adherence: number;
          voice_signature: number;
          decision_pattern: number;
          relationship_handling: number;
          preference_respect: number;
          refusal_pattern: number;
        };
      }[];
    };
  };

  const embedded = {
    character_id: parsed.methodology.character,
    character_name: "Adira (synthetic fixture)",
    ran_at: parsed.methodology.ran_at,
    judge_model: parsed.methodology.judge,
    participants: parsed.methodology.participants,
    scene_labels: parsed.methodology.scenes,
    cross_provider_variance: parsed.aggregate.cross_provider_variance,
    cross_provider_stddev: parsed.aggregate.cross_provider_stddev,
    per_provider: parsed.aggregate.per_provider,
  };

  const lines = [
    "// AUTO-GENERATED by scripts/embed-benchmark-results.ts.",
    "// Source: docs/character-emergence-results.json",
    "// Regenerate by re-running the benchmark + the embed script.",
    "",
    "import type { CrossModelBenchmarkResult } from \"../../components/Inspector/CrossModelResultsSection\";",
    "",
    "export const PUBLISHED_BENCHMARK: CrossModelBenchmarkResult = " +
      JSON.stringify(embedded, null, 2) +
      ";",
    "",
  ];
  await writeFile(TS_OUT, lines.join("\n"), "utf8");
  console.log(`wrote ${path.relative(ROOT, TS_OUT)}`);
}

main().catch((e) => {
  console.error("embed failed:", e);
  process.exit(1);
});
